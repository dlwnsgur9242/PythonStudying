def fn(x):
    n = len(x)

    for i in range(1, n):
        now = x[i]
        j = i-1
        while j >= 0 and x[j] > now:
            x[j+1]=x[j]
            j-=1
        x[j+1]=now
        # print(x) 계산과정 확인용 print문
x = [5, 8, 2, 1, 4, 3]
fn(x)
print(x)


